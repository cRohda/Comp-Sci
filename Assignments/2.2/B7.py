my_string = "InCrEdIbLe"  # Create string variable

var1 = (my_string[:5]).upper()  # Make Var1 and assign it to first 5 characters of string, makes them uppercase
print(var1)  # Prints var1

var2 = (my_string[6:]).lower()  # Make Var2 and assign it to last 4 characters of string, make them lowercase
print(var2)  # Prints var2

var3 = var1 + var2  # Makes Var3 the combination of Var1 and Var2
print(var3)  # Prints Var3